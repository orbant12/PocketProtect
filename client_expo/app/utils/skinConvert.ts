

export const SkinNumber_Convert = (skinNumber: number) => {
    switch (skinNumber) {
        case 0:
        return "#fde3ce";
        case 1:
        return "#fbc79d";
        case 2:
        return "#934506";
        case 3:
        return "#843722";
        default:
        return "#fde3ce";
    }
}


